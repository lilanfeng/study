public class _016_构成正方形的数量 {
    /**
     *题目描述
     * 输入N个互不相同的二维整数坐标，求这N个坐标可以构成的正方形数量。[内积为零的的两个向量垂直]
     * 输入描述
     * 第一行输入为N，N代表坐标数量，N为正整数。N <= 100
     * 之后的 K 行输入为坐标x y以空格分隔，x，y为整数，-10<=x, y<=10
     * 输出描述
     * 输出可以构成的正方形数量。
     *
     * 示例1
     * 输入
     * 3
     * 1 3
     * 2 4
     * 3 1
     * 输出
     * 0
     * 说明
     * （3个点不足以构成正方形）
     * 示例2
     * 输入
     *
     * 4
     * 0 0
     * 1 2
     * 3 1
     * 2 -1
     * 输出
     * 1
     * 说明
     *
     * 解题思路
     * 内积的定义
     * 内积（Dot Product）是向量代数中的一个重要概念。给定两个向量 a = ( a 1 , a 2 )  = (a_1, a_2) 和 b = ( b 1 , b 2 ) = (b_1, b_2)，它们的内积定义为：
     *
     * a ⋅ b = a 1 × b 1 + a 2 × b 2
     * 如果两个向量的内积为零，即 a ⋅ b = 0 ，那么这两个向量互相垂直（即成90度角）。
     *
     * 解题思路
     * 遍历所有点对，寻找可能的正方形：
     *
     * 对于每一对点 (A,B)，我们假设这两个点为正方形的对角线的两个端点。
     * 通过计算向量 A B 的垂直向量，可以找到另外两个点的坐标。
     * 检查这两个点是否都在集合中，如果在，则构成一个正方形。
     * 避免重复计算：
     *
     * 在统计正方形时，每个正方形会被计算四次（每个顶点都可能作为起始点），所以最终的正方形数量应该除以4。
     * 用例解释
     * 输入
     * 4
     * 0 0
     * 1 2
     * 3 1
     * 2 -1
     * 坐标点
     * A (0, 0)
     * B (1, 2)
     * C (3, 1)
     * D (2, -1)
     * 计算过程
     * 遍历点对：
     *
     * 遍历所有的点对，检查是否能构成正方形。
     * 共有 6 对点组合：(A, B), (A, C), (A, D), (B, C), (B, D), (C, D)。
     * 判断正方形：
     *
     * 以点对 (A, B) 为例：
     * 计算可能的两个正方形对角点：
     * (x3, y3) = (-2, 1) 和 (x4, y4) = (3, 1)。
     * (x5, y5) = (-2, -1) 和 (x6, y6) = (-1, 1)。
     * 检查这些点是否存在于输入的坐标集合中：
     * (x3, y3) 不存在，但 (x4, y4) 即 C 存在。
     * (x5, y5) 即 D 存在，但 (x6, y6) 不存在。
     * 因此，通过 (A, B) 这一对，找到了一个正方形 ABCD。
     * 结果：
     *
     * 通过遍历所有点对，只找到一个正方形 ABCD，最终正方形数量为 1。
     *
     */
}
