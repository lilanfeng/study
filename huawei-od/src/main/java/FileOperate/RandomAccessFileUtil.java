package FileOperate;

/**
 * 大文件处理
 */
public class RandomAccessFileUtil {

    public static void main(String[] args) {

        int i = 6;
        int j = 6;
        System.out.println((i++)+(++j));
        int c = j++;

        System.out.println(c);
        int d = ++i;
        System.out.println(d);
    }
}
